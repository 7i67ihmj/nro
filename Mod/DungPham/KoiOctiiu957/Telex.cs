			"ắs" => str.Substring(0, length - 2) + "ăs", 
			"ăf" => str.Substring(0, length - 2) + "ằ", 
			"ằf" => str.Substring(0, length - 2) + "ăf", 
			"ăr" => str.Substring(0, length - 2) + "ẳ", 
			"ẳr" => str.Substring(0, length - 2) + "ăr", 
			"ăx" => str.Substring(0, length - 2) + "ẵ", 
			"ẵ" => str.Substring(0, length - 2) + "ăx", 
			"ăj" => str.Substring(0, length - 2) + "ặ", 
			"ặj" => str.Substring(0, length - 2) + "ăj", 
			"aa" => str.Substring(0, length - 2) + "â", 
			"âa" => str.Substring(0, length - 2) + "aa", 
