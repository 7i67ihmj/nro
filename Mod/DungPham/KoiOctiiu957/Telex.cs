namespace Mod.DungPham.KoiOctiiu957;

public class Telex
{
	public static string ConvertToVietnamese(string str)
	{
		int length = str.Length;
		if (str.Length < 2)
			return str;
		return str.Substring(length - 2) switch
		{
			"as" => str.Substring(0, length - 2) + "á", 
			"ás" => str.Substring(0, length - 2) + "as", 
			"af" => str.Substring(0, length - 2) + "à", 
			"àf" => str.Substring(0, length - 2) + "af", 
