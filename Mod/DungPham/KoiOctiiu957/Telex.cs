			"ũx" => str.Substring(0, length - 2) + "ux", 
			"uj" => str.Substring(0, length - 2) + "ụ", 
			"ụj" => str.Substring(0, length - 2) + "uj", 
			"uw" => str.Substring(0, length - 2) + "ư", 
			"ưw" => str.Substring(0, length - 2) + "uu", 
			"ưs" => str.Substring(0, length - 2) + "ứ", 
			"ứs" => str.Substring(0, length - 2) + "ưs", 
			"ưf" => str.Substring(0, length - 2) + "ừ", 
			"ừf" => str.Substring(0, length - 2) + "ưf", 
			"ưr" => str.Substring(0, length - 2) + "ử", 
			"ửr" => str.Substring(0, length - 2) + "ưr", 
			"ưx" => str.Substring(0, length - 2) + "ữ", 
			"ữx" => str.Substring(0, length - 2) + "ưx", 
			"ưj" => str.Substring(0, length - 2) + "ự", 
			"ựj" => str.Substring(0, length - 2) + "ưj", 
			"ys" => str.Substring(0, length - 2) + "ý", 
			"ýs" => str.Substring(0, length - 2) + "ys", 
			"yf" => str.Substring(0, length - 2) + "ỳ", 
			"ỳf" => str.Substring(0, length - 2) + "yf", 
			"yr" => str.Substring(0, length - 2) + "ỷ", 
			"ỷr" => str.Substring(0, length - 2) + "yr", 
			"yx" => str.Substring(0, length - 2) + "ỹ", 
			"ỹx" => str.Substring(0, length - 2) + "yx", 
			"yj" => str.Substring(0, length - 2) + "ỵ", 
			"ỵj" => str.Substring(0, length - 2) + "yj", 
			"dd" => str.Substring(0, length - 2) + "đ", 
			"đd" => str.Substring(0, length - 2) + "dd", 
			_ => str, 
		};
	}
}
