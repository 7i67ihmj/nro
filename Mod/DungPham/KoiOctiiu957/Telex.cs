			"ấs" => str.Substring(0, length - 2) + "âs", 
			"âf" => str.Substring(0, length - 2) + "ầ", 
			"ầf" => str.Substring(0, length - 2) + "âf", 
			"âr" => str.Substring(0, length - 2) + "ẩ", 
			"ẩr" => str.Substring(0, length - 2) + "âr", 
			"âx" => str.Substring(0, length - 2) + "ẫ", 
			"ẫx" => str.Substring(0, length - 2) + "âx", 
			"âj" => str.Substring(0, length - 2) + "ậ", 
			"ậj" => str.Substring(0, length - 2) + "âj", 
			"es" => str.Substring(0, length - 2) + "é", 
			"és" => str.Substring(0, length - 2) + "es", 
			"ef" => str.Substring(0, length - 2) + "è", 
			"èf" => str.Substring(0, length - 2) + "ef", 
			"er" => str.Substring(0, length - 2) + "ẻ", 
			"ẻr" => str.Substring(0, length - 2) + "er", 
			"ex" => str.Substring(0, length - 2) + "ẽ", 
			"ẽx" => str.Substring(0, length - 2) + "ex", 
			"ej" => str.Substring(0, length - 2) + "ẹ", 
			"ẹj" => str.Substring(0, length - 2) + "ej", 
			"ee" => str.Substring(0, length - 2) + "ê", 
			"êe" => str.Substring(0, length - 2) + "ee", 
			"ês" => str.Substring(0, length - 2) + "ế", 
			"ếs" => str.Substring(0, length - 2) + "ês", 
			"êf" => str.Substring(0, length - 2) + "ề", 
			"ềf" => str.Substring(0, length - 2) + "êf", 
			"êr" => str.Substring(0, length - 2) + "ể", 
